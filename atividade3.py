# explique porque quando tenha mais de um atributo(variável), torna-se dificil/complicado o uso de arquivos txt para guardar informações

#Quando há muitos atributos (variáveis), usar arquivos .txt se torna complicado porque:
#Falta de estrutura — Não garante que todas as linhas tenham os mesmos dados.
#Inconsistência — Dados podem estar desorganizados ou incompletos.
#Leitura difícil — Requer mais código para interpretar corretamente.
#Pouco prático em larga escala — Difícil de buscar e manter com muitos registros.
#Sem tipos de dados — Tudo é tratado como texto, o que limita a organização.
# Por isso, é melhor usar CSV, JSON ou banco de dados, que são mais estruturados e confiáveis.